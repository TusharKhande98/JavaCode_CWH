// WHILE LOOP

public class CWH_21CH5_Loops {
    public static void main(String[] args) {
        System.out.println("here we print upto 100 by using loop");
        int i =1;
        while (i<6){
            System.out.println(i);
            i++;
        }
        System.out.println("finished running while loop");

        int j =0;
        while (j<=10){
            System.out.println(j);
            j += 2;
        }
        System.out.println("here we print 2,4,6,8,10");

    }
}



